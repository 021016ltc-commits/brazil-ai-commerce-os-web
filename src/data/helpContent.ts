export function getPageHelp(pathname: string) {
  const exact = pageHelpContent[pathname];
  if (exact) return exact;

  const matchedPath = Object.keys(pageHelpContent)
    .sort((left, right) => right.length - left.length)
    .find((path) => pathname.startsWith(`${path}/`));

  return matchedPath ? pageHelpContent[matchedPath] : defaultHelp;
}
